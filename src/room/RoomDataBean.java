package room;

public class RoomDataBean {
	private int r_id;
	private int r_maxperson;
	private int r_minperson;
	private int r_price;
	private String r_content;
	
	public int getR_id() {
		return r_id;
	}
	public void setR_id(int r_id) {
		this.r_id = r_id;
	}
	public int getR_maxperson() {
		return r_maxperson;
	}
	public void setR_maxperson(int r_maxperson) {
		this.r_maxperson = r_maxperson;
	}
	public int getR_minperson() {
		return r_minperson;
	}
	public void setR_minperson(int r_minperson) {
		this.r_minperson = r_minperson;
	}
	public int getR_price() {
		return r_price;
	}
	public void setR_price(int r_price) {
		this.r_price = r_price;
	}
	public String getR_content() {
		return r_content;
	}
	public void setR_content(String r_content) {
		this.r_content = r_content;
	}
	
	
}
